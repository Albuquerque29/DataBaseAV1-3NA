-- SALDO DE GOLS DAS SELEÇÕES ORDENADO DE FORMA DECRESCENTE
SELECT Nome_Sel, (Gols_Ft - Gols_Sof) AS Saldo_Gols FROM Selecoes ORDER BY Saldo_Gols DESC;

--LISTA TODOS OS JOGADORES QUE MARCARAM GOLS EM UMA SELEÇÃO ESPECÍFICA
SELECT Nome_Jog, Gols_Jog
FROM Jogadores
WHERE Nome_Sel = 'Argentina' AND Gols_Jog > 0;

--SELECIONA TODAS AS SELEÇÕES ORDENADAS PELO NÚMERO DE GOLS DE FORMA ASCENDENTE 
SELECT * FROM Selecoes ORDER BY Gols_Ft ASC;

--SELECIONA O NOME DAS SELEÇÕES E A QUANTIDADE DE GOLS FEITOS E SOFRIDOS
SELECT Nome_Sel, Gols_Ft, Gols_Sof FROM Selecoes;

--SELECIONA O TEMPO DOS GOLS MARCADOS E A CONDIÇÃO DO GOL SE FOI FEITO POR PENALTI OU CONTRA
Select  Tempo_GoL, Tipo_Gol from gols_selecao

-- CALCULA A MÉDIA DE GOLS DOS JOGADORES DE UM DETERMINADA POSIÇÃO DA SUA SELEÇÃO
SELECT AVG(Gols_Jog) AS Media_Gols_Atacantes
FROM Jogadores
WHERE Nome_Sel = 'Alemanha' AND Posicao = 'FW';

--SELECIONA TODOS OS ATACANTES QUE TENHAM MARCADO MAIS DE 2 GOLS
SELECT *FROM Jogadores WHERE Posicao = 'FW' AND Gols_Jog > 2;

-- SELECIONA OS JOGADORES DE UMA SELEÇÃO ESPECÍFICA
SELECT * FROM Jogadores WHERE Nome_Sel = 'Brasil';

--SELECIONA OS JOGADORES DE UMA POSIÇÃO ESPECÍFICA
SELECT * FROM Jogadores WHERE Posicao = 'GK';

-- SELECIONA SELEÇÕES E SEUS RESPECTIVOS CONTINENTES ORDENANDAS ALFABETICAMENTE
SELECT Nome_Sel, Continente FROM Selecoes ORDER BY Nome_Sel

-- CONTAR QUANTOS ESTÁDIOS ESTÃO LOCALIZADOS EM CADA ESTADO
